namespace BarcodeDemo
{
    /// <summary>
    /// Specifies available dot types of matrix barcode.
    /// </summary>
    public enum BarcodeMatixDotType
    {
        /// <summary>
        /// The dot is represented by a square.
        /// </summary>
        Square,

        /// <summary>
        /// The dot is represented by the beveled square.
        /// </summary>
        BeveledSquare,

        /// <summary>
        /// The dot is represented by a circle.
        /// </summary>
        Circle,
        
        /// <summary>
        /// The dot is represented by a rhombus.
        /// </summary>
        Rhombus,

        /// <summary>
        /// The dot is represented by a cross.
        /// </summary>
        Cross,

        /// <summary>
        /// The dot is represented by a diagonal cross.
        /// </summary>
        DiagonalCross,
    }
}
